def send_template_update_notification(changes, errors):
    """
    Send notification about template updates to Slack.
    
    Args:
        changes (list): List of template changes detected
        errors (list): List of errors encountered
    
    Returns:
        bool: True if sent successfully, False otherwise
    """
    # Determine color based on errors
    color = "#ff9900" if errors else "#36a64f"
    
    # Build message sections
    blocks = [
        {
            "type": "header",
            "text": {
                "type": "plain_text",
                "text": "ðŸ”„ GDPR Template Update",
                "emoji": True
            }
        }
    ]
    
    # Add changes section
    if changes:
        changes_text = "\n".join([f"â€¢ {change}" for change in changes])
        blocks.append({
            "type": "section",
            "text": {
                "type": "mrkdwn",
                "text": f"*ðŸ“ Changes Detected:*\n{changes_text}"
            }
        })
    
    # Add errors section
    if errors:
        errors_text = "\n".join([f"â€¢ {error}" for error in errors])
        blocks.append({
            "type": "section",
            "text": {
                "type": "mrkdwn",
                "text": f"*âš ï¸ Errors Encountered:*\n{errors_text}"
            }
        })
    
    # Add footer
    blocks.extend([
        {
            "type": "divider"
        },
        {
            "type": "context",
            "elements": [
                {
                    "type": "mrkdwn",
                    "text": "ðŸ¤– Automated notification from GDPR Compliance Checker"
                }
            ]
        }
    ])
    
    fallback_message = f"Template Update: {len(changes)} changes detected"
    if errors:
        fallback_message += f", {len(errors)} errors"
    
    return send_slack_notification(message=fallback_message, blocks=blocks)


